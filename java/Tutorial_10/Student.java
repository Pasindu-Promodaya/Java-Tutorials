package Tutorial_10;

public class Student {
    //Encapsulation
    private String name;
    private long id;

    //Create getter and setter
    public String getName() { //Get name
        return name;
    }

    public void setName(String name) { //Set name
        this.name = name;
    }

    public long getId() { //Get id
        return id;
    }

    public void setId(long id) { //Set id
        this.id = id;
    }
}
